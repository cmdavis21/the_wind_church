import { defineField } from 'sanity';

export const EventSchema = {
  name: 'event',
  title: 'Events',
  type: 'document',
  fields: [
    defineField({
      name: 'name',
      title: 'Name',
      type: 'string',
      validation: (rule) => rule.required().error('Enter the event name.'),
    }),
    defineField({
      name: 'description',
      title: 'Description',
      type: 'text',
      description: 'Describe the event.',
    }),
    defineField({
      name: 'date',
      title: 'Date',
      type: 'date',
      validation: (rule) => rule.required().error('Enter the event date.'),
    }),
    defineField({
      name: 'time',
      title: 'Time',
      description: 'What time is the event',
      type: 'timeType',
      validation: (rule) => rule.required().error('Enter the event time.'),
    }),
    defineField({
      name: 'categories',
      title: 'Categories',
      type: 'array',
      description: 'Add any categories/key-terms relating to this event.',
      of: [{ type: 'string' }],
      options: {
        list: [
          'Worship Service',
          'Prayer Meeting',
          'Bible Study',
          'Revival/Crusade',
          'Fasting & Prayer',
          'Church Fellowship',
          'Potluck / Shared Meals',
          'Game Night / Movie Night',
          'Church Picnic',
          'Small Group Gathering',
          'Community Outreach',
          'Evangelism',
          'Food / Clothing Drive',
          'Volunteer Service',
          'Youth Ministry',
          'Young Adults Fellowship',
          "Children's Church",
          'Leadership Training',
          'Discipleship Class',
          'New Members Class',
          'Ministry Workshops',
          'Sunday School',
          'Holiday Celebration',
          'Church Anniversary',
          'Fundraiser',
          'Guest Speaker / Concert',
          'Baptism / Baby Dedication',
          "Men's Ministry",
          "Women's Ministry",
          'Marriage / Couples Retreat',
          'Family Day',
        ],
        layout: 'grid',
      },
    }),
    defineField({
      name: 'image',
      type: 'image',
      title: 'Image',
      description: 'Image representing the event.',
      fields: [
        {
          name: 'alt',
          type: 'string',
          title: 'Alt',
          validation: (rule) =>
            rule
              .required()
              .error('Please add an alt description; this is helpful for seo and accessibility.'),
        },
      ],
      validation: (rule) => rule.required().error('Please add an image representing the event.'),
    }),
    defineField({
      name: 'location',
      type: 'string',
      title: 'Location',
      description: 'Where is the event happening.',
      validation: (rule) => rule.required().error('Enter where the event is happening.'),
    }),
    defineField({
      name: 'how_to_signup',
      type: 'string',
      title: 'How to Signup',
      description: 'Instructions for how to join/participate in this event.',
    }),
    defineField({
      name: 'contact',
      title: 'Contact',
      description: 'Instructions for who to contact regarding this event.',
      type: 'reference',
      to: [{ type: 'leader' }],
      validation: (rule) => rule.required().error('Select a contact responsible for this event.'),
    }),
    defineField({
      name: 'ministry_event',
      title: 'Ministry Event',
      type: 'reference',
      to: [{ type: 'ministry' }],
      description: 'Select if a Ministry is the organizer/host for this event.',
    }),
    defineField({
      name: 'external_host',
      type: 'string',
      title: 'External Host',
      description: 'Is there an external organization/host for this event?',
    }),
    defineField({
      name: 'help_needed',
      type: 'array',
      title: 'Help Needed',
      description: 'List any open positions for help during this event.',
      of: [{ type: 'string' }],
    }),
    defineField({
      name: 'cost',
      type: 'string',
      title: 'Cost',
      description:
        'Are there any costs associated with participation in this event? (e.g., $40.00 per Adult ticket)',
    }),
  ],
};
